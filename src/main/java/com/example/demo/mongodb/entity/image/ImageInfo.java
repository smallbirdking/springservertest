package com.example.demo.mongodb.entity.image;

public class ImageInfo {
  String url;
  String imgType;

  public String getUrl() {
    return url;
  }

  public void setUrl(String url) {
    this.url = url;
  }

  public String getImgType() {
    return imgType;
  }

  public void setImgType(String imgType) {
    this.imgType = imgType;
  }
}
